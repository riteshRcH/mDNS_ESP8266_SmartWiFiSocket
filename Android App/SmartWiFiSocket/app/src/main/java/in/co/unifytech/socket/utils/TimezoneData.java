    public static boolean performChecks(Context context)
    {
        boolean somethingAbsent = false;

        // every region shown in expandable listview must be present in regionToTZIDs
        for (Map.Entry<String, List<String>> entry: expandableListViewData.entrySet())
            for (String region:entry.getValue())
            {
                if (!regionToTZIDs.containsKey(region))
                {
                    Log.d("Display Absent Region", region);
                    somethingAbsent = true;
                }
            }

        if (!somethingAbsent)
        {
            // every region in regionToTZIDs must be present in expandable listview
            for(Map.Entry<String, String> entry:regionToTZIDs.entrySet())
            {
                boolean presentInOneOfTheContinent = false;
                for (Map.Entry<String, List<String>> entry1: expandableListViewData.entrySet())
                    if (entry1.getValue().contains(entry.getKey()))
                        presentInOneOfTheContinent = true;

                if (!presentInOneOfTheContinent)
                {
                    somethingAbsent = true;
                    Log.d("Display Absent Region", entry.getKey());
                }
            }
        }

        if (!somethingAbsent)
        {
            // every value of localtime contents must be present in assets
            for (Map.Entry<String, String> entry : regionToLocaltimeContents.entrySet())
                if (!"NA".equalsIgnoreCase(entry.getValue()) && !AssetsUtil.checkIfFileExistsInAssets(context, "zoneinfo/" + entry.getValue()))
                {
                    somethingAbsent = true;
                    Log.d("Absent file", "assets/zoneinfo/" + entry.getValue());
                }
        }

        if (!somethingAbsent)
        {
            // every key of regionToTZIDs must map to a value in regionToTZIDs and regionToLocaltimeContents
            for (Map.Entry<String, String> entry : regionToTZIDs.entrySet())
            {
                String tzid = regionToTZIDs.get(entry.getKey());
                String localtime = regionToLocaltimeContents.get(entry.getKey());

                if (tzid == null)
                {
                    somethingAbsent = true;
                    Log.d("Region2TZID Absent", entry.getKey());
                }
                if (localtime == null)
                {
                    somethingAbsent = true;
                    Log.d("Region2localtime Absent", entry.getKey());
                }
            }
        }

        if (!somethingAbsent)
        {
            // every key of regionToLocaltimeContents must map to a value in regionToTZIDs and regionToLocaltimeContents
            for (Map.Entry<String, String> entry : regionToLocaltimeContents.entrySet())
            {
                String tzid = regionToTZIDs.get(entry.getKey());
                String localtime = regionToLocaltimeContents.get(entry.getKey());

                if (tzid == null)
                {
                    somethingAbsent = true;
                    Log.d("Region2TZID Absent", entry.getKey());
                }
                if (localtime == null)
                {
                    somethingAbsent = true;
                    Log.d("Region2localtime Absent", entry.getKey());
                }
            }
        }

        if (!somethingAbsent)
        {
            // Every key in regionToTZIDs must map to something and that something must map back to key in TZIDsToRegion
            for (Map.Entry<String, String> entry : regionToTZIDs.entrySet())
            {
                if (!entry.getKey().equals(TZIDsToRegion.get(regionToTZIDs.get(entry.getKey()))))
                {
                    somethingAbsent = true;
                    Log.d("No Region->TZID->Region", entry.getKey());
                }
            }
        }

        if (!somethingAbsent)
        {
            // Every TZID in TZIDsToRegion must be contained in only 1 value of localtimeContentsToTZIDs, then get that corresponding key and check against phone timezone
            for (Map.Entry<String, String> entry : TZIDsToRegion.entrySet())
            {
                int cnter = 0;
                for (Map.Entry<String, List<String>> entry1 : localtimeContentsToTZIDs.entrySet())
                {
                    if (entry1.getValue().contains(entry.getKey()))
                        cnter++;
                }

                if (cnter != 1)
                {
                    somethingAbsent = true;
                    Log.d("No Mapping", entry.getKey() + " (" + Integer.toString(cnter) + ")");
                }
            }
        }

        return !somethingAbsent;    //not somethingAbsent = all present => all checks done
    }
